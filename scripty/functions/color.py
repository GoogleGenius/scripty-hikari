class Color:
    @classmethod
    def blurple(cls):
        return cls(0x5865F2)

    @classmethod
    def green(cls):
        return cls(0x57F287)

    @classmethod
    def yellow(cls):
        return cls(0xFEE75C)

    @classmethod
    def fuchsia(cls):
        return cls(0xEB459E)

    @classmethod
    def red(cls):
        return cls(0xED4245)

    @classmethod
    def white(cls):
        return cls(0xFFFFFF)

    @classmethod
    def black(cls):
        return cls(0x000000)
