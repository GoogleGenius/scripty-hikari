class Color:
    @classmethod
    def blurple(cls):
        return 0x5865F2

    @classmethod
    def green(cls):
        return 0x57F287

    @classmethod
    def yellow(cls):
        return 0xFEE75C

    @classmethod
    def fuchsia(cls):
        return 0xEB459E

    @classmethod
    def red(cls):
        return 0xED4245

    @classmethod
    def white(cls):
        return 0xFFFFFF

    @classmethod
    def black(cls):
        return 0x000000
