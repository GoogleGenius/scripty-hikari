class Color:
    @staticmethod
    def blurple():
        return 0x5865F2

    @staticmethod
    def green():
        return 0x57F287

    @staticmethod
    def yellow():
        return 0xFEE75C

    @staticmethod
    def fuchsia():
        return 0xEB459E

    @staticmethod
    def red():
        return 0xED4245

    @staticmethod
    def white():
        return 0xFFFFFF

    @staticmethod
    def black():
        return 0x000000
